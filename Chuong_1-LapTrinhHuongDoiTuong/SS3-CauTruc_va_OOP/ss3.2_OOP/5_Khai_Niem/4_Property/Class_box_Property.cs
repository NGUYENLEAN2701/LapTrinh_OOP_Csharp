class Box
{
    //! fields
    private double _length;
    private double _breadth;
    private double _height;

    // properties
    public double Length
    {
        get
        {
            return _length;
        }
        set
        {
            _length = value;
        }
    }

    public double Breadth
    {
        get
        {
            return _breadth;
        }
        set
        {
            _breadth = value;
        }
    }

    public double Height
    {
        get
        {
            return _height;
        }
        set
        {
            _height = value;
        }
    }
}